def test_mixed_transaction_history_and_holdings_split_correctly(tmp_path: Path):
    vault = tmp_path
    raw = vault / "70_Imports" / "raw"
    raw.mkdir(parents=True)
    pd.DataFrame([
        {"실거래일자": "2026-04-29", "거래유형": "매수", "상세내용": "코스피매수", "종목명": "삼성전자 005930", "수량": "1", "거래금액": "70000", "잔고금액": "70000"},
    ]).to_excel(raw / "종합거래내역 상세.xlsx", index=False)
    pd.DataFrame([
        {"종목명": "삼성전자", "종목코드": "005930", "보유수량": "2", "평가금액": "140000", "평가손익": "1000", "평가손익률": "0.71"},
    ]).to_excel(raw / "종합잔고.xlsx", index=False)
    import_raw_dir(vault)
    processed = vault / "70_Imports" / "processed"
    idx = pd.read_csv(processed / "source_file_index.csv")
    tx = pd.read_csv(processed / "processed_transactions.csv")
    holdings = pd.read_csv(processed / "processed_holdings.csv")
    assert set(idx["source_file_type"]) == {"transaction_history", "holdings"}
    assert len(tx) == 1
    assert len(holdings) == 1
    assert set(holdings["source_file_type"]) == {"holdings"}


