from __future__ import annotations

import argparse
from pathlib import Path

from obsidian_writer import bootstrap_autogenerated_block


def resolve_company_note(vault_root: Path, target: str) -> Path:
    """Resolve an explicitly approved Company.md target inside the vault."""
    root = vault_root.resolve()
    path = (root / target).resolve()
    try:
        rel = path.relative_to(root)
    except ValueError as exc:
        raise ValueError(f"target is outside vault root: {target}") from exc
    if len(rel.parts) != 3 or rel.parts[0] != "20_Companies" or rel.name != "Company.md":
        raise ValueError(f"target must be an explicit 20_Companies/<ticker>/Company.md path: {target}")
    return path


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Bootstrap AUTO-GENERATED markers for explicitly approved managed company notes.")
    parser.add_argument("--vault-root", default=str(Path(__file__).resolve().parents[2]), help="06_Stock Vault root")
    parser.add_argument("--target", action="append", required=True, help="Explicit relative target, e.g. 20_Companies/005930/Company.md")
    parser.add_argument("--heading", default="Auto-generated update area", help="Heading inserted above the AUTO-GENERATED block")
    parser.add_argument("--dry-run", action="store_true", help="Validate targets without writing files")
    return parser


def main() -> int:
    args = build_parser().parse_args()
    vault_root = Path(args.vault_root).resolve()
    failed = False
    for target in args.target:
        try:
            path = resolve_company_note(vault_root, target)
            ok, warning = bootstrap_autogenerated_block(path, heading=args.heading, dry_run=args.dry_run)
        except Exception as exc:
            ok, warning = False, str(exc)
        status = "OK" if ok else "FAIL"
        mode = "dry-run" if args.dry_run else "write"
        print(f"[{status}] {mode} {target}{': ' + warning if warning else ''}")
        failed = failed or not ok
    return 1 if failed else 0


if __name__ == "__main__":
    raise SystemExit(main())
