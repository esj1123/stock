from __future__ import annotations

import re
from datetime import date
from pathlib import Path
from typing import Any

import pandas as pd

from nh_importer import detect_sensitive, is_cash_asset_name, is_leveraged_etf_name, safe_source_name
from obsidian_writer import AUTO_END, AUTO_START, replace_autogenerated_block, markdown_table
from portfolio_model import has_filled_section, parse_frontmatter


def read_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        return pd.DataFrame()
    try:
        return pd.read_csv(path, dtype={"ticker": str}, encoding="utf-8-sig")
    except Exception:
        return pd.DataFrame()


def add(rows: list[dict[str, Any]], exception_id: str, severity: str, file: str, issue: str, suggested_fix: str) -> None:
    rows.append({"exception_id": exception_id, "severity": severity, "file": file, "issue": issue, "suggested_fix": suggested_fix})


def days_old(value: Any) -> int | None:
    if value is None or str(value).strip() == "":
        return None
    try:
        parsed = pd.to_datetime(value)
        if pd.isna(parsed):
            return None
        dt = parsed.date()
    except Exception:
        return None
    return (date.today() - dt).days


def generated_blocks(text: str) -> list[str]:
    return re.findall(re.escape(AUTO_START) + r"(.*?)" + re.escape(AUTO_END), text, re.S)


def is_cash_company_note(path: Path, meta: dict[str, Any]) -> bool:
    asset_type = str(meta.get("asset_type", "") or "").strip().lower()
    ticker = str(meta.get("ticker", "") or path.parent.name).strip()
    name = str(meta.get("name", "") or path.parent.name).strip()
    return asset_type == "cash" or is_cash_asset_name(name, ticker)


def run_qa(vault_root: Path, processed_dir: Path | None = None, dry_run: bool = False) -> pd.DataFrame:
    processed_dir = processed_dir or vault_root / "70_Imports" / "processed"
    rows: list[dict[str, Any]] = []

    company_files = list((vault_root / "20_Companies").glob("*/Company.md"))
    for path in company_files:
        text = path.read_text(encoding="utf-8-sig")
        meta = parse_frontmatter(text)
        rel = str(path.relative_to(vault_root))
        typ = meta.get("type") or meta.get("doc_type")
        is_cash_note = is_cash_company_note(path, meta)
        if "type" not in meta and not is_cash_note:
            add(rows, "INV-EX-01", "blocking", rel, "필수 YAML type이 없습니다.", "frontmatter에 type: company를 추가하세요.")
        if typ in {"company", "holding_stock", "holding_etf"} and not is_cash_note:
            if not meta.get("ticker") or not meta.get("market"):
                add(rows, "INV-EX-02", "blocking", rel, "ticker 또는 market이 비어 있습니다.", "ticker와 market을 채우세요.")
            if not has_filled_section(text, ["투자 논리", "Thesis", "매수 이유"]):
                add(rows, "INV-EX-03", "advisory", rel, "thesis가 비어 있거나 placeholder 수준입니다.", "투자 논리/매수 이유를 작성하세요.")
            if not has_filled_section(text, ["매도", "비중축소", "sell criteria", "청산 조건"]):
                add(rows, "INV-EX-08", "blocking", rel, "sell criteria가 비어 있습니다.", "매도/비중축소 조건을 작성하세요.")
            if not meta.get("source_files") and "원천자료" not in text:
                add(rows, "INV-EX-07", "advisory", rel, "source link가 없습니다.", "source_files 또는 원천자료 링크를 추가하세요.")
            old = days_old(meta.get("last_review") or meta.get("last_update"))
            if old is not None and old > 30:
                add(rows, "INV-EX-06", "advisory", rel, f"last_review가 {old}일 전입니다.", "리뷰 노트를 갱신하세요.")
            if is_leveraged_etf_name(meta.get("name", ""), meta.get("ticker", "")) and "leveraged_etf_rule_link" not in meta:
                add(rows, "INV-EX-05", "blocking", rel, "레버리지 ETF 규칙 링크가 없습니다.", "leveraged_etf_rule_link를 추가하세요.")
        for block in generated_blocks(text):
            if detect_sensitive(block):
                add(rows, "INV-EX-12", "blocking", rel, "AUTO-GENERATED 블록에 민감정보 후보가 있습니다.", "generated output redaction을 확인하세요.")

    for path in (vault_root / "10_Dashboard").glob("*.md"):
        text = path.read_text(encoding="utf-8-sig")
        if path.name in {"Portfolio.md", "Exposure.md", "Cashflows.md", "Import_Review.md", "Risk_Watchlist.md", "Review_Queue.md", "QA_Exceptions.md"}:
            if AUTO_START not in text or AUTO_END not in text:
                add(rows, "INV-EX-09", "blocking", str(path.relative_to(vault_root)), "자동화 대상 파일에 AUTO-GENERATED 마커가 없습니다.", "마커를 추가하거나 수동 파일로 유지하세요.")
        for block in generated_blocks(text):
            if detect_sensitive(block):
                add(rows, "INV-EX-12", "blocking", str(path.relative_to(vault_root)), "대시보드 generated block에 민감정보 후보가 있습니다.", "redaction을 적용하세요.")

    holdings = read_csv(processed_dir / "processed_holdings.csv")
    if not holdings.empty:
        for _, r in holdings.iterrows():
            ticker = str(r.get("ticker", ""))
            asset_type = str(r.get("asset_type", "") or "").strip().lower()
            if asset_type == "cash":
                continue
            pnl = pd.to_numeric(pd.Series([r.get("pnl_pct", 0)]), errors="coerce").fillna(0).iloc[0]
            if pnl <= -10:
                add(rows, "INV-EX-04", "blocking", f"processed_holdings.csv:{ticker}", "pnl_pct <= -10이며 최근 리뷰 확인이 필요합니다.", "Risk Event 또는 Review Report를 작성하세요.")
            if is_leveraged_etf_name(r.get("security_name", ""), ticker):
                note = vault_root / "20_Companies" / ticker / "Company.md"
                if not note.exists() or "leveraged_etf_rule_link" not in note.read_text(encoding="utf-8-sig"):
                    add(rows, "INV-EX-05", "blocking", f"processed_holdings.csv:{ticker}", "레버리지 ETF 규칙 링크가 없습니다.", "Leveraged_ETF_Rules 링크를 추가하세요.")

    unclassified = read_csv(processed_dir / "unclassified_rows.csv")
    if not unclassified.empty:
        add(rows, "INV-EX-11", "blocking", "70_Imports/processed/unclassified_rows.csv", f"unclassified row {len(unclassified)}건이 있습니다.", "거래유형 분류 규칙을 보완하세요.")

    source_index = read_csv(processed_dir / "source_file_index.csv")
    indexed = set(source_index.get("source_file", pd.Series(dtype=str)).astype(str)) if not source_index.empty else set()
    raw_dir = vault_root / "70_Imports" / "raw"
    for raw in raw_dir.glob("*"):
        if raw.suffix.lower() not in {".xls", ".xlsx"}:
            continue
        if safe_source_name(raw) not in indexed:
            add(rows, "INV-EX-10", "advisory", str(raw.relative_to(vault_root)), "raw brokerage file이 source_file_index.csv에 없습니다.", "import를 다시 실행하세요.")

    result = pd.DataFrame(rows, columns=["exception_id", "severity", "file", "issue", "suggested_fix"])
    if not dry_run:
        processed_dir.mkdir(parents=True, exist_ok=True)
        result.to_csv(processed_dir / "qa_exceptions.csv", index=False, encoding="utf-8-sig")
        dash = vault_root / "10_Dashboard" / "QA_Exceptions.md"
        replace_autogenerated_block(dash, markdown_table(result, limit=200), dry_run=False)
    return result
